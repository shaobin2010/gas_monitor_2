
#include "utils.h"

typedef union FLOAT_CONV
{
    float f;
    char c[4];
} float_conv;

uint16_t utils_checksum(uint8_t *buff, uint32_t len)
{
    uint32_t i = 0;
    uint16_t sum = 0;
    for (i = 0; i < len; i++){
        sum += buff[i];
    }

    return sum;
}


float float_little_bit(float value)
{
    float_conv d1,d2;
    d1.f = value;
    d2.c[0] = d1.c[3];
    d2.c[1] = d1.c[2];
    d2.c[2] = d1.c[1];
    d2.c[3] = d1.c[0];
    return d2.f;
}


